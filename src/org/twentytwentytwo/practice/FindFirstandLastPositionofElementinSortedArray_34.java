  public int[] searchRange(int[] nums, int target) {
    int[] finalArray = new int[]{-1, -1};
    int l = 0;
    int h = nums.length - 1;
    while (l <= h) {
      int mid = l + (h - l) / 2;
      if (nums[mid] == target) {
        int found1 = binarySearch(nums, target, l, mid - 1, true, mid);
        int found2 = binarySearch(nums, target, mid + 1, nums.length - 1, false, mid);
        if (found1 != -1 && found2 != -1) {
          finalArray[0] = found1;
          finalArray[1] = found2;
        } else if (found1 == -1 && found2 == -1) {
          finalArray[0] = mid;
          finalArray[1] = mid;
        } else if (found1 != -1) {
          finalArray[0] = found1;
          finalArray[1] = mid;
        } else {
          finalArray[0] = mid;
          finalArray[1] = found2;
        }
        break;
      } else if (nums[mid] < target) {
        l = mid + 1;
      } else {
        h = mid - 1;
      }
    }

    return finalArray;
  }

  private int binarySearch(int[] nums, int target, int l,
      int h, boolean leftSearch, int current) {
    while (l <= h) {
      int mid = l + (h - l) / 2;
      if (nums[mid] == target) {
        if (leftSearch) {
          return Math.min(mid, binarySearch(nums, target, l, mid - 1, true, mid));
        } else {
          return Math.max(mid, binarySearch(nums, target, mid + 1, nums.length - 1, false, mid));
        }
      } else if (nums[mid] < target) {
        l = mid + 1;
      } else {
        h = mid - 1;
      }
    }
    return current;
  }


