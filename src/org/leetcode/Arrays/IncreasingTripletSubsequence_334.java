  public boolean increasingTriplet(int[] nums) {

    if (nums.length < 3) {
      return false;
    }

    int firstMin = Integer.MAX_VALUE;
    int secondMin = Integer.MAX_VALUE;

    for (int i = 0; i < nums.length; i++) {

      if (nums[i] < firstMin) {
        firstMin = nums[i];
      } else if (nums[i] > firstMin && nums[i] < secondMin) {
        secondMin = nums[i];
      } else if (nums[i] > secondMin) {
        return true;
      }

    }
    return false;

  }


